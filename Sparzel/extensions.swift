extension String {
    mutating func stripFromCharacter(char:String) {
        let c = self.characters
        if var ix = c.index(of: ".") {
            ix = c.index(after: ix)
            self = String(c.suffix(from: ix))
        }
    }
}

extension UITableViewCell {
